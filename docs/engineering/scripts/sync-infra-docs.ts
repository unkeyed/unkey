import { Glob } from "bun";
import { $ } from "bun";
import { resolve, relative, dirname } from "node:path";

const ROOT = resolve(import.meta.dir, "..");
const DOCS_JSON = resolve(ROOT, "docs.json");
const DEST_DIR = resolve(ROOT, "infra");

const REPO = "unkeyed/infra";
const SRC_DIR = "docs";
const TAB_NAME = "Infra";
const GROUP_NAME = "Infra";

// --- Fetch files from GitHub ---

interface TreeEntry {
  path: string;
  type: string;
}

async function fetchFiles() {
  console.log(`Fetching docs from ${REPO}/${SRC_DIR}...`);

  const destDir = Bun.file(DEST_DIR);
  console.log(`Clearing ${DEST_DIR}...`);
  await $`rm -rf ${DEST_DIR}`;
  await $`mkdir -p ${DEST_DIR}`;
  await Bun.write(
    resolve(DEST_DIR, ".gitignore"),
    `# The content of this folder generated by running bun \`scripts/sync-infra-docs.ts\`. DO NOT EDIT.
# To change these, go to github.com/unkeyed/infra

*
!.gitignore
`,
  ); // ensure directory exists for nav update

  const tree: { tree: TreeEntry[] } =
    await $`gh api repos/${REPO}/git/trees/main?recursive=4`.json();

  const files = tree.tree.filter((e) => e.type === "blob" && e.path.startsWith(`${SRC_DIR}/`));

  for (const file of files) {
    const relPath = file.path
      .slice(SRC_DIR.length + 1)
      .toLowerCase()
      .replaceAll("_", "-");
    const destPath = resolve(DEST_DIR, relPath);
    await $`mkdir -p ${dirname(destPath)}`;

    const resp: { content: string } = await $`gh api repos/${REPO}/contents/${file.path}`.json();
    const decoded = Buffer.from(resp.content, "base64");
    await Bun.write(destPath, decoded);
    console.log(`  ${relPath}`);
  }

  console.log(`Fetched ${files.length} files.`);
}

// --- Update navigation in docs.json ---

type NavEntry = string | { group: string; pages: NavEntry[] };

async function scanPages(): Promise<string[]> {
  const glob = new Glob("**/*.{md,mdx}");
  const pages: string[] = [];
  for await (const path of glob.scan(DEST_DIR)) {
    const rel = relative(ROOT, resolve(DEST_DIR, path));
    pages.push(rel.replace(/\.mdx?$/, ""));
  }
  return pages.sort();
}

function buildTree(pages: string[], basePrefix: string): NavEntry[] {
  const topLevel: string[] = [];
  const subdirs = new Map<string, string[]>();

  for (const page of pages) {
    const rel = page.slice(basePrefix.length + 1);
    const slashIdx = rel.indexOf("/");
    if (slashIdx === -1) {
      topLevel.push(page);
    } else {
      const dir = rel.slice(0, slashIdx);
      if (!subdirs.has(dir)) subdirs.set(dir, []);
      subdirs.get(dir)!.push(page);
    }
  }

  const result: NavEntry[] = [...topLevel];
  for (const [dir, dirPages] of subdirs) {
    result.push({
      group: dir,
      pages: buildTree(dirPages, `${basePrefix}/${dir}`),
    });
  }

  return result;
}

async function updateNav() {
  const docsFile = Bun.file(DOCS_JSON);
  const docs = await docsFile.json();
  const pages = await scanPages();

  if (pages.length === 0) {
    console.error("No pages found after sync.");
    process.exit(1);
  }

  const basePrefix = relative(ROOT, DEST_DIR);
  const navPages = buildTree(pages, basePrefix);

  const infraTab = docs.navigation.tabs.find((t: any) => t.tab === TAB_NAME);
  if (!infraTab) {
    console.error(`Tab "${TAB_NAME}" not found in docs.json`);
    process.exit(1);
  }

  const group = { group: GROUP_NAME, expanded: true, pages: navPages };
  const idx = infraTab.groups.findIndex((g: any) => g.group === GROUP_NAME);
  if (idx >= 0) {
    infraTab.groups[idx] = group;
  } else {
    infraTab.groups.push(group);
  }

  await Bun.write(DOCS_JSON, JSON.stringify(docs, null, 2) + "\n");
  console.log(`Updated docs.json: ${pages.length} pages in "${GROUP_NAME}"`);
}

// --- Run ---

await fetchFiles();
await updateNav();
