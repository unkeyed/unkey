// Command exportoneof scans protobuf-generated Go files for unexported oneof
// interfaces (e.g. isClusterMessage_Message) and creates companion files that
// re-export them as public type aliases (e.g. IsClusterMessage_Message).
//
// Usage:
//
//	go run ./tools/exportoneof <directory>
package main

import (
	"bufio"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"strings"
)

var oneofPattern = regexp.MustCompile(`^type (is[A-Z]\w+) interface \{$`)

type oneofInterface struct {
	pkg        string
	unexported string
	exported   string
}

func main() {
	if len(os.Args) < 2 {
		fmt.Fprintln(os.Stderr, "usage: exportoneof <directory>")
		os.Exit(1)
	}
	root := os.Args[1]

	packages := map[string][]oneofInterface{}

	err := filepath.Walk(root, func(path string, info os.FileInfo, err error) error {
		if err != nil || info.IsDir() || !strings.HasSuffix(path, ".pb.go") {
			return nil
		}

		f, err := os.Open(path)
		if err != nil {
			return nil
		}
		defer f.Close()

		dir := filepath.Dir(path)
		scanner := bufio.NewScanner(f)

		var pkgName string
		for scanner.Scan() {
			line := scanner.Text()
			if strings.HasPrefix(line, "package ") {
				pkgName = strings.TrimPrefix(line, "package ")
			}

			if m := oneofPattern.FindStringSubmatch(line); m != nil {
				unexported := m[1]
				exported := "I" + unexported[1:] // isXxx â†’ IsXxx
				packages[dir] = append(packages[dir], oneofInterface{
					pkg:        pkgName,
					unexported: unexported,
					exported:   exported,
				})
			}
		}
		return nil
	})
	if err != nil {
		fmt.Fprintf(os.Stderr, "walk error: %v\n", err)
		os.Exit(1)
	}

	for dir, ifaces := range packages {
		if err := writeFile(dir, ifaces); err != nil {
			fmt.Fprintf(os.Stderr, "error writing %s: %v\n", dir, err)
			os.Exit(1)
		}
	}
}

func writeFile(dir string, ifaces []oneofInterface) error {
	path := filepath.Join(dir, "oneof_interfaces.go")
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	fmt.Fprintf(f, "// Code generated by tools/exportoneof. DO NOT EDIT.\n\n")
	fmt.Fprintf(f, "package %s\n", ifaces[0].pkg)

	for _, iface := range ifaces {
		fmt.Fprintf(f, "\n// %s is the exported form of the protobuf oneof interface %s.\n", iface.exported, iface.unexported)
		fmt.Fprintf(f, "type %s = %s\n", iface.exported, iface.unexported)
	}

	return nil
}
