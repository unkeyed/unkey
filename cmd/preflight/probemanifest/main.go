// Command probemanifest emits the list of registered preflight probes as
// a sorted, newline-delimited manifest and optionally verifies that the
// committed manifest matches the current code.
//
// Purpose: runbooks for each probe live in the infra repo, next to the
// PrometheusRule yaml that carries their runbook_url. The unkey repo
// owns the source of truth for probe NAMES via the init() registrations.
// This tool is the bridge: it writes a plain-text manifest that infra CI
// consumes to assert every probe has a runbook.
//
// Modes:
//
//	--write     regenerate cmd/preflight/probes/MANIFEST.txt from the registry.
//	--check     exit non-zero if the committed manifest disagrees with the
//	            registry (the default; what CI runs).
//
// Why a committed manifest rather than a live probe-list RPC: it makes
// probe additions visible in unkey PR diffs, keeps the infra repo
// buildable against a pinned unkey commit, and needs no network access
// at CI time.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"path/filepath"

	// Blank-import svc/preflight so every probe package's init() runs
	// and registers itself. Without this, probes.Names() would return
	// an empty slice and the manifest would silently wipe itself.
	_ "github.com/unkeyed/unkey/svc/preflight"
	"github.com/unkeyed/unkey/svc/preflight/probes"
)

// manifestPath is relative to the repo root. Committed alongside the
// probe files so additions and removals land in the same PR.
const manifestPath = "svc/preflight/probes/MANIFEST.txt"

const header = "# Auto-generated by cmd/preflight/probemanifest. Run `go run ./cmd/preflight/probemanifest --write` to regenerate.\n"

func main() {
	var write bool
	flag.BoolVar(&write, "write", false, "Write MANIFEST.txt instead of checking it.")
	flag.Parse()

	want := render(probes.Names())

	if write {
		if err := os.WriteFile(manifestPath, want, 0o644); err != nil {
			fmt.Fprintf(os.Stderr, "probemanifest: %v\n", err)
			os.Exit(2)
		}
		fmt.Printf("probemanifest: wrote %d probe(s) to %s\n", len(probes.Names()), manifestPath)
		return
	}

	got, err := os.ReadFile(manifestPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "probemanifest: cannot read %s: %v\n", manifestPath, err)
		fmt.Fprintf(os.Stderr, "Fix: go run ./cmd/preflight/probemanifest --write\n")
		os.Exit(1)
	}
	if !bytes.Equal(got, want) {
		fmt.Fprintln(os.Stderr, "probemanifest: committed manifest is out of date")
		fmt.Fprintln(os.Stderr)
		fmt.Fprintln(os.Stderr, "--- committed")
		fmt.Fprintln(os.Stderr, string(got))
		fmt.Fprintln(os.Stderr, "--- live")
		fmt.Fprintln(os.Stderr, string(want))
		fmt.Fprintln(os.Stderr, "Fix: go run ./cmd/preflight/probemanifest --write")
		os.Exit(1)
	}
	fmt.Printf("probemanifest: %d probe(s) OK (%s)\n", len(probes.Names()), filepath.Base(manifestPath))
}

func render(names []string) []byte {
	var b bytes.Buffer
	b.WriteString(header)
	for _, n := range names {
		b.WriteString(n)
		b.WriteByte('\n')
	}
	return b.Bytes()
}
